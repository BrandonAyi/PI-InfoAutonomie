package model;

/**
 * Description of the sensor type.
 * Created by Mathieu on 31/01/2015.
 */
public enum SensorType {
    /**
     * Light Sensor.
     */
    LIGHT,
    /**
     * Temperature sensor.
     */
    TEMP,
    /**
     * Humidity sensor.
     */
    HUMIDITY,
    /**
     * Presence sensor.
     */
    PRESENCE,
    /**
     * Door or window sensor (open/closed).
     */
    DOOR,
    /**
     * Electricity power consumption sensor.
     */
    POWER
}
